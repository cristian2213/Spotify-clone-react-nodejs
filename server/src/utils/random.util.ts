  public getRandomSongs(totalSongs: number): Array<string> {
    let limit = totalSongs;
    let i = 1;
    const songs = [];
    while (limit >= i) {
      const random = Math.round(Math.random() * this.randomSongs.length);

      const songID = this.randomSongs[random];
      const isValidID = ytdl.validateID(songID);

      if (!isValidID) continue;
      songs.push(songID);
      i++;
    }
    return songs;
  }
